package routers

import (
	"net/http"

	"github.com/Thenecromance/BlizzardAPI/app"
	"github.com/Thenecromance/BlizzardAPI/api/wow/DataService/Pet"

	"github.com/gin-gonic/gin"
)

func init() {

	app.Instance().RegisterRoute("GET", "/data/wow/pet/index", ginwowPetsIndex) /* PetsIndex Returns an index of battle pets. */

	app.Instance().RegisterRoute("GET", "/data/wow/pet/:petId", ginwowPet) /* Pet Returns a battle pets by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/media/pet/:petId", ginwowPetMedia) /* PetMedia Returns media for a battle pet by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/pet-ability/index", ginwowPetAbilitiesIndex) /* PetAbilitiesIndex Returns an index of pet abilities. */

	app.Instance().RegisterRoute("GET", "/data/wow/pet-ability/:petAbilityId", ginwowPetAbility) /* PetAbility Returns a pet ability by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/media/pet-ability/:petAbilityId", ginwowPetAbilityMedia) /* PetAbilityMedia Returns media for a pet ability by ID. */

}




