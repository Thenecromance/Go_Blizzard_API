package routers

import (
	"net/http"

	"github.com/Thenecromance/BlizzardAPI/app"
	"github.com/Thenecromance/BlizzardAPI/api/StarCraftII/Community/Profile"

	"github.com/gin-gonic/gin"
)

func init() {

	app.Instance().RegisterRoute("GET", "/sc2/static/profile/:regionId", ginStarCraftIIStatic) /* Static Returns all static SC2 profile data (achievements, categories, criteria, and rewards). */

	app.Instance().RegisterRoute("GET", "/sc2/metadata/profile/:regionId/:realmId/:profileId", ginStarCraftIIMetadata) /* Metadata Returns metadata for an individual's profile. */

	app.Instance().RegisterRoute("GET", "/sc2/profile/:regionId/:realmId/:profileId", ginStarCraftIIProfile) /* Profile Returns data about an individual SC2 profile. */

	app.Instance().RegisterRoute("GET", "/sc2/profile/:regionId/:realmId/:profileId/ladder/summary", ginStarCraftIILadderSummary) /* LadderSummary Returns a ladder summary for an individual SC2 profile. */

	app.Instance().RegisterRoute("GET", "/sc2/profile/:regionId/:realmId/:profileId/ladder/:ladderId", ginStarCraftIILadder) /* Ladder Returns data about an individual profile's ladder. */

}




