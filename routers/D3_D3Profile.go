func init() {

	app.Instance().RegisterRoute("GET", "/d3/profile/:account/", ginD3GetApiAccount) /* GetApiAccount Returns the specified account profile. */

	app.Instance().RegisterRoute("GET", "/d3/profile/:account/hero/:heroId", ginD3GetApiHero) /* GetApiHero Returns a single hero. */

	app.Instance().RegisterRoute("GET", "/d3/profile/:account/hero/:heroId/items", ginD3GetApiDetailedHeroItems) /* GetApiDetailedHeroItems Returns a list of items for the specified hero. */

	app.Instance().RegisterRoute("GET", "/d3/profile/:account/hero/:heroId/follower-items", ginD3GetApiDetailedFollowerItems) /* GetApiDetailedFollowerItems Returns a list of items for the specified hero's followers. */

}




