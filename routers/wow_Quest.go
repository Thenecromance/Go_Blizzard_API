package routers

import (
	"net/http"

	"github.com/Thenecromance/BlizzardAPI/app"
	"github.com/Thenecromance/BlizzardAPI/api/wow/DataService/Quest"

	"github.com/gin-gonic/gin"
)

func init() {

	app.Instance().RegisterRoute("GET", "/data/wow/quest/index", ginwowQuestsIndex) /* QuestsIndex Returns the parent index for quests. */

	app.Instance().RegisterRoute("GET", "/data/wow/quest/:questId", ginwowQuest) /* Quest Returns a quest by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/quest/category/index", ginwowQuestCategoriesIndex) /* QuestCategoriesIndex Returns an index of quest categories (such as quests for a specific class, profession, or storyline). */

	app.Instance().RegisterRoute("GET", "/data/wow/quest/category/:questCategoryId", ginwowQuestCategory) /* QuestCategory Returns a quest category by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/quest/area/index", ginwowQuestAreasIndex) /* QuestAreasIndex Returns an index of quest areas. */

	app.Instance().RegisterRoute("GET", "/data/wow/quest/area/:questAreaId", ginwowQuestArea) /* QuestArea Returns a quest area by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/quest/type/index", ginwowQuestTypesIndex) /* QuestTypesIndex Returns an index of quest types (such as PvP quests, raid quests, or account quests). */

	app.Instance().RegisterRoute("GET", "/data/wow/quest/type/:questTypeId", ginwowQuestType) /* QuestType Returns a quest type by ID. */

}




