package routers

import (
	"net/http"

	"github.com/Thenecromance/BlizzardAPI/app"
	"github.com/Thenecromance/BlizzardAPI/api/wow/DataService/Covenant"

	"github.com/gin-gonic/gin"
)

func init() {

	app.Instance().RegisterRoute("GET", "/data/wow/covenant/index", ginwowCovenantIndex) /* CovenantIndex Returns an index of covenants. */

	app.Instance().RegisterRoute("GET", "/data/wow/covenant/:covenantId", ginwowCovenant) /* Covenant Returns a covenant by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/media/covenant/:covenantId", ginwowCovenantMedia) /* CovenantMedia Returns media for a covenant by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/covenant/soulbind/index", ginwowSoulbindIndex) /* SoulbindIndex Returns an index of soulbinds. */

	app.Instance().RegisterRoute("GET", "/data/wow/covenant/soulbind/:soulbindId", ginwowSoulbind) /* Soulbind Returns a soulbind by ID. */

	app.Instance().RegisterRoute("GET", "/data/wow/covenant/conduit/index", ginwowConduitIndex) /* ConduitIndex Returns an index of conduits. */

	app.Instance().RegisterRoute("GET", "/data/wow/covenant/conduit/:conduitId", ginwowConduit) /* Conduit Returns a conduit by ID. */

}




